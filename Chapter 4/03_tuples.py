# a = () # empty tuple
# a = (1,) # single element tuple
a = (1,455,67,"Ayush",False,"Harsh", 67)

# Note: Tuple is immutable.

# (ii) Tuple Methods:
num = a.count(67) # returns number of times 67 occurs in a tuple
print(num)

index = a.index(67) # returns the index of first occurrence of 67 in a tuple 
print(index)